package de.suzufa.screwbox.core.graphics;

/**
 * Some predefined mouse cursors that you can use.
 * 
 * @see Window#setCursor(PredefinedCursor)
 * @see Window#setFullscreenCursor(PredefinedCursor)
 * @see Window#setWindowCursor(PredefinedCursor)
 */
public enum PredefinedCursor {

    /**
     * The default cursor of the current operation system.
     */
    DEFAULT,

    /**
     * Not visible cursor.
     */
    HIDDEN;
}
