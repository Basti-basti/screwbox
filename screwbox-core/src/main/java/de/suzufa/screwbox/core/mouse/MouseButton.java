package de.suzufa.screwbox.core.mouse;

/**
 * The mouse-Buttons.
 * 
 * @see Mouse#isButtonDown(MouseButton)
 * @see Mouse#justPressed(MouseButton)
 *
 */
public enum MouseButton {

    /**
     * The left mouse button.
     */
    LEFT,

    /**
     * The middle mouse button.
     */
    MIDDLE,

    /**
     * The right mouse button.
     */
    RIGHT;
}
