package io.github.srcimon.screwbox.core.mouse;

/**
 * The mouse-Buttons.
 * 
 * @see Mouse#isDown(MouseButton)
 * @see Mouse#clicked(MouseButton)
 *
 */
public enum MouseButton {

    /**
     * The left mouse button.
     */
    LEFT,

    /**
     * The middle mouse button.
     */
    MIDDLE,

    /**
     * The right mouse button.
     */
    RIGHT
}
